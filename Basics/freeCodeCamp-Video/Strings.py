# functions that are specific to another function are methods (examples belwo)
print("1", "Giraffe\nAcademy")  # \n = new line
Name = "Danny Hatcher"

print("2", Name.lower())  # Making it all lower
print("3", Name.capitalize())  # Capetalize first word
print("4", Name.upper())  # Making it all upper
print("5", Name.islower())  # Checking if it is all lower
print("6", Name.isupper())  # checking if it is all upper
print("7", Name.upper().isupper())  # Make it upper and check it is upper
print("8", len(Name))  # Count character length
print("9", Name[0])  # Prints the number in the string (starting with 0)
print("10", Name.index("D"))  # Gives the first location in the string
print("11", Name.replace("Danny", "Jade"))  # Replacing characters with others
